    private String MsgCampos() {
        String s;
        s = "Os campos a seguir não podem ficar em branco: \n \n";
        for (int i = 0; i < Campos.size(); i++) {
            s += Campos.get(i) + " \n";
        }

        return s;
    }

    private void ValidaCampos() {
        Campos.clear();
        if (txtNome.getText().length() == 0) {
            Campos.add("Nome");
        }
        if ("   .   .   -  ".equals(txtCpf.getText())) {
            Campos.add("CPF");
        }
        if ((txtSenha.getText().length() == 0) || (txtConfSenha.getText().length() == 0)) {
            Campos.add("Senha");
        }
    }

    private void CarregarPropriedades() {
        txtCodigo.setText(Integer.toString(c.getCodigo()));
        txtNome.setText(c.getNome());
        txtCpf.setText(c.getCpf());
        txtSenha.setText(c.getSenha());
        txtConfSenha.setText(txtSenha.getText());
        edicao = true;
    }

