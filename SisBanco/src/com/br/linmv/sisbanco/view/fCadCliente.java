    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        ValidaCampos();
        if (Campos.size() > 0) {
            JOptionPane.showMessageDialog(null, MsgCampos(), "Erro...", JOptionPane.ERROR_MESSAGE);
        } else {
            int Resul = JOptionPane.showConfirmDialog(null, "Deseja salvar o cliente " + txtNome.getText() + " ?", "Confirmação...", JOptionPane.YES_NO_OPTION);
            if (Resul == JOptionPane.YES_OPTION) {

                if (!edicao) {
                    c = new Cliente(txtNome.getText(), txtCpf.getText(), txtSenha.getText());
                    this.dispose();
                    if (callback != null) {
                        callback.clienteCadastradoCall(c);
                    }
                } else {
                    c.setNome(txtNome.getText());
                    c.setSenha(txtSenha.getText());
                    this.dispose();
                    if (callback != null) {
                        callback.clienteEditadoCall(c);
                    }
                }
            }
        }
    }//GEN-LAST:event_btnGravarActionPerformed

    private boolean Valida_Senhas() {
        if ((txtSenha.getText().length() > 0) && (txtConfSenha.getText().length() > 0) && (!txtSenha.getText().equals(txtConfSenha.getText()))) {
            JOptionPane.showMessageDialog(null, "As senhas não conferem!", "Erro...", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }

    private void IniciaEdicao() {
        CarregarPropriedades();
        int i = 0;
        while (i < 3) {
            
            JPasswordField password = new JPasswordField(10);
            password.setEchoChar('*');
            JLabel rotulo = new JLabel("Confirme sua senha:");  
            JPanel entUsuario = new JPanel();
            entUsuario.add(rotulo);
            entUsuario.add(password);
            
            JOptionPane.showMessageDialog(null, entUsuario, "Acesso restrito", JOptionPane.PLAIN_MESSAGE);   
            String senha = password.getText();
            
            if(!senha.equals(c.getSenha()))
                i++;
            else
                break;
        }
        if (i == 3)
            this.dispose();
        else
        txtCpf.setEditable(false);
    }

